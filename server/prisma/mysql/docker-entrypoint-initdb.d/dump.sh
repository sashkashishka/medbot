#!/bin/bash

echo "** Creating default DB and users"

mysql -u root -p${MYSQL_ROOT_PASSWORD} --execute "CREATE DATABASE IF NOT EXISTS $MYSQL_DATABASE;"

mysql -u root -p${MYSQL_ROOT_PASSWORD} --execute "CREATE USER IF NOT EXISTS '$MYSQL_USER'@'%' IDENTIFIED BY '$MYSQL_PASSWORD';"

mysql -u root -p${MYSQL_ROOT_PASSWORD} --execute \
"GRANT ALTER,CREATE,DELETE,DROP,INDEX,INSERT,SELECT,UPDATE,TRIGGER,ALTER ROUTINE, CREATE ROUTINE, EXECUTE, CREATE TEMPORARY TABLES ON $MYSQL_DATABASE.* TO '$MYSQL_USER'@'%';"

echo "** Finished creating default DB and users"
